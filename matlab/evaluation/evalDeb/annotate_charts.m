function img_annot = annotate_charts(img, data, n_charts)
% Verify manual marking

LINEW = 3;
FONTSIZE = 30;
img_annot = img;

for i_chart = 1:n_charts
    x_colors = round(data(i_chart).horizontal);
    y_colors = round(data(i_chart).vertical);
    position = [x_colors, y_colors, 6*ones(length(x_colors), 1)];
    
    % Insert circles for all colors (mark in red)
    img_annot = insertShape(img_annot, 'circle', position, ...
        'LineWidth', LINEW, 'Color', 'red');
    
    % Mark numbers on the rightmost and leftmost columns
    % Whtie path - mark in black and white text
    dist = 40;
    img_annot = insertObjectAnnotation(img_annot, 'circle', position(1,:), ...
        '1', 'LineWidth', LINEW, 'Color', 'black', 'TextColor', 'white','FontSize', FONTSIZE);
    img_annot = insertObjectAnnotation(img_annot, 'circle', position(6,:), ...
        '6', 'LineWidth', LINEW, 'Color', 'white', 'TextColor', 'black', 'FontSize', FONTSIZE);
    img_annot = insertText(img_annot, [x_colors(7) + dist, y_colors(7)], '7', ...
        'BoxColor', 'magenta', 'TextColor', 'black', ...
        'FontSize', FONTSIZE, 'AnchorPoint', 'RightCenter');
    img_annot = insertText(img_annot, [x_colors(12) - 2*dist, y_colors(12)], '12', ...
        'BoxColor', 'red', 'TextColor', 'black', ...
        'FontSize', FONTSIZE, 'AnchorPoint', 'LeftCenter');
    img_annot = insertText(img_annot, [x_colors(13) - 2*dist, y_colors(13)+dist], '13', ...
        'BoxColor', 'red', 'TextColor', 'black', ...
        'FontSize', FONTSIZE, 'AnchorPoint', 'LeftCenter');
    img_annot = insertText(img_annot, [x_colors(18) + dist, y_colors(18)+dist/2], '18', ...
        'BoxColor', 'yellow', 'TextColor', 'black', ...
        'FontSize', FONTSIZE, 'AnchorPoint', 'RightTop');
end
